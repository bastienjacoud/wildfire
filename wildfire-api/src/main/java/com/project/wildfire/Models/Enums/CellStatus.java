package com.project.wildfire.Models.Enums;

/**
 * Enumeration for cell's status
 */
public enum CellStatus {
    /**
     * Default status
     */
    DEFAULT,

    /**
     * Status for trees
     */
    TREE,

    /**
     * Status for fire
     */
    FIRE,

    /**
     * Status for cinders
     */
    CINDER
}
